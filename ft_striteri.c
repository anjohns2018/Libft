#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int		i;

	if (!s || !f)
		return ;
	i = -1;
	if (s && f)
		while (*(s + ++i))
			f(i, s + i);
}
